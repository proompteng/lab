"""Measure Torghut Python tech-debt counters and enforce ratchets."""

from __future__ import annotations

import argparse
import ast
import io
import json
import re
import subprocess
import sys
import tokenize
from collections import Counter, defaultdict
from collections.abc import Mapping, Sequence
from dataclasses import dataclass
from pathlib import Path
from typing import cast

SCHEMA_VERSION = "torghut.tech-debt-measurement.v1"
DEFAULT_PATHS = ("app", "scripts", "tests")
FILE_LINE_THRESHOLDS = (1000,)
FUNCTION_LINE_THRESHOLDS = (100, 250, 500, 750)
CLASS_LINE_THRESHOLDS = (300, 500, 750)
PYLINT_DESIGN_CODES = ("R0911", "R0912", "R0913", "R0914", "R0915", "R0917", "R1702")
PYLINT_DESIGN_RULES = (
    "too-many-arguments",
    "too-many-positional-arguments",
    "too-many-branches",
    "too-many-locals",
    "too-many-return-statements",
    "too-many-statements",
    "too-many-nested-blocks",
)

MARKERS: Mapping[str, str] = {
    "typing_any_import": "from typing import Any",
    "broad_exception": "except Exception",
    "base_exception": "except BaseException",
    "time_sleep": "time.sleep",
    "asyncio_sleep": "asyncio.sleep",
    "not_implemented_error": "NotImplementedError",
    "type_ignore": "type: ignore",
    "pyright_comment": "# pyright:",
    "noqa_comment": "# noqa",
}
COMMENT_MARKERS: Mapping[str, str] = {
    "type_ignore": "type: ignore",
    "pyright_comment": "# pyright:",
    "noqa_comment": "# noqa",
}

_PYLINT_MESSAGE_RE = re.compile(
    r"^(?P<path>[^:]+):(?P<line>\d+):(?P<column>\d+): "
    r"(?P<code>[A-Z]\d+): "
)


@dataclass(frozen=True)
class RatchetViolation:
    metric: str
    baseline: int
    current: int

    def as_message(self) -> str:
        return f"{self.metric}: current={self.current} baseline={self.baseline}"


def main(argv: Sequence[str] | None = None) -> int:
    args = _parse_args(argv)
    root = Path(args.root).resolve()
    measurement = measure_torghut_debt(
        root=root,
        paths=tuple(args.paths),
        include_pylint_design=not args.skip_pylint_design,
    )
    if args.baseline and args.enforce_ratchet:
        baseline = cast(
            Mapping[str, object],
            json.loads(Path(args.baseline).read_text(encoding="utf-8")),
        )
        violations = ratchet_violations(measurement, baseline)
        if violations:
            print("Torghut tech-debt ratchet violations:")
            for violation in violations:
                print(f"- {violation.as_message()}")
            return 16

    if args.format == "json":
        print(format_json(measurement))
    else:
        print(format_markdown(measurement))
    return 0


def _parse_args(argv: Sequence[str] | None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--root",
        default=".",
        help="Torghut service root. Defaults to the current directory.",
    )
    parser.add_argument(
        "--paths",
        nargs="+",
        default=list(DEFAULT_PATHS),
        help="Torghut-relative files or directories to measure.",
    )
    parser.add_argument(
        "--format",
        choices=("json", "markdown"),
        default="json",
        help="Output format.",
    )
    parser.add_argument(
        "--baseline",
        help="Baseline JSON generated by this script.",
    )
    parser.add_argument(
        "--enforce-ratchet",
        action="store_true",
        help="Fail when ratcheted debt metrics increase over --baseline.",
    )
    parser.add_argument(
        "--skip-pylint-design",
        action="store_true",
        help="Skip Pylint design inventory. Intended only for unit-test fixtures.",
    )
    return parser.parse_args(argv)


def measure_torghut_debt(
    *,
    root: Path,
    paths: Sequence[str] = DEFAULT_PATHS,
    include_pylint_design: bool = True,
) -> dict[str, object]:
    root_counts: dict[str, dict[str, int]] = {}
    threshold_counts = {
        "files": _threshold_counter("files_ge", FILE_LINE_THRESHOLDS),
        "functions": _threshold_counter("functions_ge", FUNCTION_LINE_THRESHOLDS),
        "classes": _threshold_counter("classes_ge", CLASS_LINE_THRESHOLDS),
    }
    marker_counts: dict[str, dict[str, object]] = {
        name: {"total": 0, "by_root": {}} for name in sorted(MARKERS)
    }
    parse_errors: list[str] = []

    for path in _python_files(root, paths):
        relative = path.relative_to(root)
        root_name = relative.parts[0] if relative.parts else "."
        text = path.read_text(encoding="utf-8", errors="ignore")
        lines = text.splitlines()
        root_summary = root_counts.setdefault(root_name, {"files": 0, "lines": 0})
        root_summary["files"] += 1
        root_summary["lines"] += len(lines)
        _increment_thresholds(threshold_counts["files"], "files_ge", len(lines))
        try:
            tree = ast.parse(text)
        except SyntaxError:
            parse_errors.append(str(relative))
            _count_comment_markers(marker_counts, root_name, text)
            continue
        _count_ast_markers(marker_counts, root_name, tree)
        _count_comment_markers(marker_counts, root_name, text)
        _count_ast_thresholds(
            tree,
            threshold_counts["functions"],
            threshold_counts["classes"],
        )

    pylint_design = (
        measure_pylint_design(root, paths=("app", "scripts"))
        if include_pylint_design
        else _empty_pylint_design()
    )
    return {
        "schema_version": SCHEMA_VERSION,
        "paths": sorted(paths),
        "roots": {key: root_counts[key] for key in sorted(root_counts)},
        "total": {
            "files": sum(item["files"] for item in root_counts.values()),
            "lines": sum(item["lines"] for item in root_counts.values()),
        },
        "threshold_counts": {
            **threshold_counts["files"],
            **threshold_counts["functions"],
            **threshold_counts["classes"],
        },
        "markers": marker_counts,
        "pylint_design": pylint_design,
        "parse_errors": sorted(parse_errors),
    }


def measure_pylint_design(
    root: Path,
    *,
    paths: Sequence[str],
) -> dict[str, object]:
    scan_paths = [path for path in paths if (root / path).exists()]
    if not scan_paths:
        return _empty_pylint_design()
    result = subprocess.run(
        [
            sys.executable,
            "-m",
            "pylint",
            *scan_paths,
            "--disable=all",
            f"--enable={','.join(PYLINT_DESIGN_RULES)}",
            "--score=n",
        ],
        cwd=root,
        check=False,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
    )
    return parse_pylint_design_output(result.stdout)


def parse_pylint_design_output(output: str) -> dict[str, object]:
    by_code: Counter[str] = Counter()
    by_file: Counter[str] = Counter()
    examples: dict[str, list[str]] = defaultdict(list)
    for line in output.splitlines():
        match = _PYLINT_MESSAGE_RE.match(line)
        if match is None:
            continue
        code = match.group("code")
        if code not in PYLINT_DESIGN_CODES:
            continue
        path = match.group("path")
        by_code[code] += 1
        by_file[path] += 1
        if len(examples[code]) < 5:
            examples[code].append(line)
    return {
        "total": sum(by_code.values()),
        "by_code": {code: by_code.get(code, 0) for code in PYLINT_DESIGN_CODES},
        "top_files": [
            {"path": path, "count": count}
            for path, count in sorted(
                by_file.items(), key=lambda item: (-item[1], item[0])
            )[:25]
        ],
        "examples": {code: examples.get(code, []) for code in PYLINT_DESIGN_CODES},
    }


def ratchet_violations(
    current: Mapping[str, object],
    baseline: Mapping[str, object],
) -> list[RatchetViolation]:
    current_metrics = ratcheted_metrics(current)
    baseline_metrics = ratcheted_metrics(baseline)
    violations: list[RatchetViolation] = []
    for metric, current_value in sorted(current_metrics.items()):
        baseline_value = baseline_metrics.get(metric)
        if baseline_value is None:
            continue
        if current_value > baseline_value:
            violations.append(
                RatchetViolation(
                    metric=metric,
                    baseline=baseline_value,
                    current=current_value,
                )
            )
    return violations


def ratcheted_metrics(measurement: Mapping[str, object]) -> dict[str, int]:
    metrics: dict[str, int] = {}
    threshold_counts = cast(
        Mapping[str, object], measurement.get("threshold_counts", {})
    )
    for key, value in threshold_counts.items():
        if isinstance(value, int):
            metrics[f"threshold_counts.{key}"] = value
    markers = cast(Mapping[str, object], measurement.get("markers", {}))
    for name, marker_payload in markers.items():
        if not isinstance(marker_payload, Mapping):
            continue
        marker_mapping = cast(Mapping[str, object], marker_payload)
        total = marker_mapping.get("total")
        if isinstance(total, int):
            metrics[f"markers.{name}.total"] = total
    pylint_design = cast(Mapping[str, object], measurement.get("pylint_design", {}))
    total = pylint_design.get("total")
    if isinstance(total, int):
        metrics["pylint_design.total"] = total
    by_code = cast(Mapping[str, object], pylint_design.get("by_code", {}))
    for code, value in by_code.items():
        if isinstance(value, int):
            metrics[f"pylint_design.by_code.{code}"] = value
    return metrics


def format_json(measurement: Mapping[str, object]) -> str:
    return json.dumps(measurement, indent=2, sort_keys=True)


def format_markdown(measurement: Mapping[str, object]) -> str:
    total = cast(Mapping[str, object], measurement["total"])
    thresholds = cast(Mapping[str, object], measurement["threshold_counts"])
    pylint_design = cast(Mapping[str, object], measurement["pylint_design"])
    markers = cast(Mapping[str, object], measurement["markers"])
    lines = [
        "# Torghut Tech Debt Measurement",
        "",
        f"- Python files: {total['files']}",
        f"- Python lines: {total['lines']}",
        f"- Pylint design findings: {pylint_design['total']}",
        "",
        "## Threshold Counts",
    ]
    for key, value in sorted(thresholds.items()):
        lines.append(f"- `{key}`: {value}")
    lines.extend(("", "## Marker Counts"))
    for key, payload in sorted(markers.items()):
        if isinstance(payload, Mapping):
            marker_payload = cast(Mapping[str, object], payload)
            lines.append(f"- `{key}`: {marker_payload.get('total', 0)}")
    return "\n".join(lines)


def _python_files(root: Path, paths: Sequence[str]) -> list[Path]:
    files: list[Path] = []
    for item in paths:
        path = root / item
        if path.is_file() and path.suffix == ".py":
            files.append(path)
        elif path.is_dir():
            files.extend(path.rglob("*.py"))
    return sorted({file.resolve() for file in files})


def _threshold_counter(prefix: str, thresholds: Sequence[int]) -> dict[str, int]:
    return {f"{prefix}_{threshold}": 0 for threshold in thresholds}


def _increment_thresholds(
    counts: dict[str, int],
    prefix: str,
    value: int,
) -> None:
    for key in counts:
        threshold = int(key.removeprefix(f"{prefix}_"))
        if value >= threshold:
            counts[key] += 1


def _count_ast_markers(
    marker_counts: dict[str, dict[str, object]],
    root_name: str,
    tree: ast.AST,
) -> None:
    for node in ast.walk(tree):
        if isinstance(node, ast.ImportFrom) and node.module == "typing":
            if any(alias.name == "Any" for alias in node.names):
                _increment_marker(marker_counts, root_name, "typing_any_import")
        elif isinstance(node, ast.ExceptHandler):
            exception_name = _exception_handler_name(node.type)
            if exception_name == "Exception":
                _increment_marker(marker_counts, root_name, "broad_exception")
            elif exception_name == "BaseException":
                _increment_marker(marker_counts, root_name, "base_exception")
        elif isinstance(node, ast.Call):
            call_name = _call_name(node.func)
            if call_name == "time.sleep":
                _increment_marker(marker_counts, root_name, "time_sleep")
            elif call_name == "asyncio.sleep":
                _increment_marker(marker_counts, root_name, "asyncio_sleep")
        elif isinstance(node, ast.Name) and node.id == "NotImplementedError":
            _increment_marker(marker_counts, root_name, "not_implemented_error")


def _count_comment_markers(
    marker_counts: dict[str, dict[str, object]],
    root_name: str,
    text: str,
) -> None:
    try:
        tokens = tokenize.generate_tokens(io.StringIO(text).readline)
        comments = [token.string for token in tokens if token.type == tokenize.COMMENT]
    except tokenize.TokenError:
        comments = []
    for comment in comments:
        for name, marker in COMMENT_MARKERS.items():
            if marker in comment:
                _increment_marker(marker_counts, root_name, name)


def _increment_marker(
    marker_counts: dict[str, dict[str, object]],
    root_name: str,
    name: str,
) -> None:
    payload = marker_counts[name]
    total = payload.get("total")
    payload["total"] = (total if isinstance(total, int) else 0) + 1
    by_root = cast(dict[str, int], payload["by_root"])
    by_root[root_name] = by_root.get(root_name, 0) + 1


def _exception_handler_name(node: ast.expr | None) -> str | None:
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.Attribute):
        return node.attr
    return None


def _call_name(node: ast.expr) -> str | None:
    if not isinstance(node, ast.Attribute) or not isinstance(node.value, ast.Name):
        return None
    return f"{node.value.id}.{node.attr}"


def _count_ast_thresholds(
    tree: ast.AST,
    function_thresholds: dict[str, int],
    class_thresholds: dict[str, int],
) -> None:
    for node in ast.walk(tree):
        end_lineno = getattr(node, "end_lineno", None)
        lineno = getattr(node, "lineno", None)
        if not isinstance(end_lineno, int) or not isinstance(lineno, int):
            continue
        length = end_lineno - lineno + 1
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            _increment_thresholds(function_thresholds, "functions_ge", length)
        elif isinstance(node, ast.ClassDef):
            _increment_thresholds(class_thresholds, "classes_ge", length)


def _empty_pylint_design() -> dict[str, object]:
    return {
        "total": 0,
        "by_code": {code: 0 for code in PYLINT_DESIGN_CODES},
        "top_files": [],
        "examples": {code: [] for code in PYLINT_DESIGN_CODES},
    }


if __name__ == "__main__":
    sys.exit(main())
