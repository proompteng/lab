describe('finalized Signal snapshot reader', () => {
  test('preserves ClickHouse retryability for check, inspect, and load failures', () => {
    for (const operation of ['check', 'inspect', 'load'] as const) {
      const authorization = marketDataOperationError(
        operation,
        'Signal query failed',
        new SqlError({
          reason: new AuthorizationError({ cause: new Error('SELECT denied'), operation: 'query' }),
        }),
      )
      const connection = marketDataOperationError(
        operation,
        'Signal query failed',
        new SqlError({
          reason: new ConnectionError({ cause: new Error('connection reset'), operation: 'query' }),
        }),
      )

      expect(authorization).toMatchObject({ component: 'market-data', operation, retryable: false })
      expect(connection).toMatchObject({ component: 'market-data', operation, retryable: true })
    }
  })

  test('reproduces the authoritative V2 manifest and snapshot identities', () => {
    const universe = ['AMD', 'AVGO', 'COHR', 'CRDO', 'LITE', 'MRVL', 'MU', 'NVDA', 'WDC'] as const
    const publication = verifyFinalizedManifest([authoritativeManifest], {
      snapshotId: authoritativeManifest.snapshot_id,
      publicationAsOf: authoritativeManifest.publication_asof,
      calendarVersion: authoritativeManifest.calendar_version,
      universeId: authoritativeManifest.universe_id,
      universeSymbolHash: authoritativeManifest.universe_symbol_hash,
      universe,
      historyStart: '2022-01-27',
      evaluationStart: '2023-01-30',
      bounds: {
        schemaVersion: 'bayn.evaluation-bounds.v1',
        dataStart: '2022-01-27',
        dataEnd: '2026-07-20',
        lookbackStart: '2022-01-27',
        evaluationStart: '2023-01-30',
        evaluationEnd: '2026-07-20',
      },
      observedAt: '2026-07-21T10:48:49.306Z',
    })

    expect(publication).toMatchObject({
      snapshotId: authoritativeManifest.snapshot_id,
      publicationId: authoritativeManifest.manifest_content_hash,
      universeId: authoritativeManifest.universe_id,
      universeSymbolHash: authoritativeManifest.universe_symbol_hash,
      symbols: universe,
      rowCount: authoritativeManifest.bar_count,
      sessionCount: authoritativeManifest.session_count,
    })
  })

  test('reproduces the publisher contract before exposing bounded numeric bars', () => {
    const fixture = makeFixture()
    const snapshot = verifyFinalizedSnapshot(fixture.rows, fixture.request)
    const source = fixture.rows.manifests[0]

    expect(snapshot.bars).toHaveLength(4)
    expect(snapshot.manifest).toMatchObject({
      schemaVersion: 'bayn.input-manifest.v3',
      rowCount: 4,
      sessionCount: 2,
      firstSession: '2025-01-02',
      lastSession: '2025-01-03',
      finalizedSnapshot: {
        snapshotId: source.snapshot_id,
        publicationId: source.manifest_content_hash,
        contentHash: source.bars_content_hash,
        sessionsContentHash: source.sessions_content_hash,
        sourceFeed: DataFeed.Sip,
        universeId: 'equity-infrastructure-v1',
        universeSymbolHash,
        symbols,
      },
    })
    expect(snapshot.manifest.hash).toMatch(/^[a-f0-9]{64}$/)
    expect(snapshot.bars.every((bar) => bar.open > 0 && bar.close > 0)).toBe(true)
  })

  test('checks the finalized publication identity without loading sessions or bars', () => {
    const fixture = makeFixture()
    const publication = verifyFinalizedManifest(fixture.rows.manifests, fixture.request)

    expect(publication).toMatchObject({
      snapshotId,
      publicationId: fixture.rows.manifests[0].manifest_content_hash,
      calendarVersion: fixture.request.calendarVersion,
      asOfSession: fixture.request.publicationAsOf,
      symbols,
      rowCount: 4,
      sessionCount: 2,
    })
    expect(() => verifyFinalizedManifest([], fixture.request)).toThrow('0 manifests; expected exactly one')
  })

  test('precommits the exact input manifest from manifest and calendar rows without bars', () => {
    const fixture = makeFixture()
    const inspection = verifyFinalizedCalendar(
      { manifests: fixture.rows.manifests, sessions: fixture.rows.sessions },
      fixture.request,
    )
    const snapshot = verifyFinalizedSnapshot(fixture.rows, fixture.request)

    expect(inspection.manifest).toEqual(snapshot.manifest)
    expect(inspection.sessionDates).toEqual(['2025-01-02', '2025-01-03'])
  })

  test('rejects duplicate manifests, sessions, and bars', () => {
    const fixture = makeFixture()
    expect(() =>
      verifyFinalizedSnapshot(
        { ...fixture.rows, manifests: [...fixture.rows.manifests, fixture.rows.manifests[0]] },
        fixture.request,
      ),
    ).toThrow('expected exactly one')
    expect(() =>
      verifyFinalizedSnapshot(
        { ...fixture.rows, sessions: [...fixture.rows.sessions, fixture.rows.sessions[0]] },
        fixture.request,
      ),
    ).toThrow('duplicate exchange session')
    expect(() =>
      verifyFinalizedSnapshot({ ...fixture.rows, bars: [...fixture.rows.bars, fixture.rows.bars[0]] }, fixture.request),
    ).toThrow('duplicate adjusted bar')
  })

  test('rejects incomplete replica reads instead of silently shrinking history', () => {
    const fixture = makeFixture()
    expect(() => verifyFinalizedSnapshot({ ...fixture.rows, manifests: [] }, fixture.request)).toThrow(
      '0 manifests; expected exactly one',
    )
    expect(() =>
      verifyFinalizedSnapshot({ ...fixture.rows, sessions: fixture.rows.sessions.slice(1) }, fixture.request),
    ).toThrow('exchange-session count does not match manifest')
    expect(() =>
      verifyFinalizedSnapshot({ ...fixture.rows, bars: fixture.rows.bars.slice(1) }, fixture.request),
    ).toThrow('adjusted-bar count does not match manifest')
  })

  test('rejects unexpected symbols and mixed publication provenance', () => {
    const fixture = makeFixture()
    const unexpected = [{ ...fixture.rows.bars[0], symbol: 'QQQ' }, ...fixture.rows.bars.slice(1)]
    expect(() => verifyFinalizedSnapshot({ ...fixture.rows, bars: unexpected }, fixture.request)).toThrow(
      'snapshot universe does not match request',
    )

    const mixed = [{ ...fixture.rows.bars[0], source_feed: 'iex' as never }, ...fixture.rows.bars.slice(1)]
    expect(() => verifyFinalizedSnapshot({ ...fixture.rows, bars: mixed }, fixture.request)).toThrow(
      'mix provider, feed, or adjustment',
    )
  })

  test('rejects a universe hash or evaluation history that differs from the compiled strategy', () => {
    const fixture = makeFixture()
    expect(() =>
      verifyFinalizedSnapshot(fixture.rows, { ...fixture.request, universeSymbolHash: '0'.repeat(64) }),
    ).toThrow('manifest universe symbol hash does not match request')
    expect(() =>
      verifyFinalizedSnapshot(fixture.rows, { ...fixture.request, historyStart: '2025-01-03' as const }),
    ).toThrow('snapshot history start does not match the compiled strategy')
  })

  test('rejects changed values, calendar contracts, counts, and hashes', () => {
    const fixture = makeFixture()
    const changed = [{ ...fixture.rows.bars[0], adjusted_close: '999.00000000' }, ...fixture.rows.bars.slice(1)]
    expect(() => verifyFinalizedSnapshot({ ...fixture.rows, bars: changed }, fixture.request)).toThrow(
      'adjusted-bar content hash is invalid',
    )

    const wrongCalendar = [
      { ...fixture.rows.sessions[0], calendar_version: 'wrong-calendar' },
      ...fixture.rows.sessions.slice(1),
    ]
    expect(() => verifyFinalizedSnapshot({ ...fixture.rows, sessions: wrongCalendar }, fixture.request)).toThrow(
      'mixes calendar versions',
    )

    const badManifest = { ...fixture.rows.manifests[0], bar_count: 7 }
    expect(() => verifyFinalizedSnapshot({ ...fixture.rows, manifests: [badManifest] }, fixture.request)).toThrow(
      'manifest content hash is invalid',
    )
  })

  test('rejects future finalization, a different publication session, and non-session bounds', () => {
    const fixture = makeFixture()
    expect(() =>
      verifyFinalizedSnapshot(fixture.rows, { ...fixture.request, observedAt: '2025-01-04T00:59:59.999Z' }),
    ).toThrow('finalization is in the future')
    expect(() => verifyFinalizedSnapshot(fixture.rows, { ...fixture.request, publicationAsOf: '2025-01-02' })).toThrow(
      'does not match expected session',
    )
    expect(() =>
      verifyFinalizedSnapshot(fixture.rows, {
        ...fixture.request,
        evaluationStart: '2025-01-04' as const,
        bounds: { ...fixture.request.bounds, evaluationStart: '2025-01-04' as const },
      }),
    ).toThrow('is not an exchange session')
  })
})
