const pickRandomIndex = (max: number) => Math.floor(Math.random() * max)

export const pickWorktreeCityName = (exclude: Set<string>): string => {
  const candidates = CITY_NAMES.filter((name) => !exclude.has(name))
  if (candidates.length === 0) {
    throw new Error('No available worktree city names remaining')
  }
  return candidates[pickRandomIndex(candidates.length)]
}
