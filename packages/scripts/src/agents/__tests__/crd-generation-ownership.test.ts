import { existsSync, readFileSync } from 'node:fs'
import { basename, dirname, join } from 'node:path'

import { describe, expect, it } from 'bun:test'
import YAML from 'yaml'

const repoRoot = process.cwd()
const crdsDir = join(repoRoot, 'charts/agents/crds')
const apiDir = join(repoRoot, 'services/agents/api')

type CrdDocument = {
  metadata?: { name?: string; annotations?: Record<string, string> }
  spec?: {
    group?: string
    names?: { kind?: string; plural?: string }
  }
}

const apiPackageFor = (group: string, kind: string) => {
  const groupPrefix = group.replace(/\.proompteng\.ai$/, '')
  if (groupPrefix === 'agents') return 'agents'
  if (groupPrefix === 'orchestration') return 'orchestration'
  if (groupPrefix === 'tools') return 'tools'
  if (groupPrefix === 'signals') return 'signals'
  if (groupPrefix === 'swarm') return 'swarm'
  if (groupPrefix === 'security') return 'security'
  if (groupPrefix === 'approvals') return 'approvals'
  if (groupPrefix === 'budgets') return 'budgets'
  if (groupPrefix === 'schedules') return 'schedules'
  if (groupPrefix === 'artifacts') return 'artifacts'
  if (groupPrefix === 'workspaces') return 'workspaces'
  throw new Error(`No services/agents/api package mapping for ${group}/${kind}`)
}

const loadCrds = () =>
  Array.from(new Bun.Glob('*.yaml').scanSync(crdsDir))
    .sort()
    .map((file) => {
      const path = join(crdsDir, file)
      const document = YAML.parse(readFileSync(path, 'utf8')) as CrdDocument
      const group = document.spec?.group
      const kind = document.spec?.names?.kind
      const plural = document.spec?.names?.plural
      if (!group || !kind || !plural) throw new Error(`CRD ${file} is missing spec.group/spec.names`)
      return { file, path, group, kind, plural, name: document.metadata?.name ?? '' }
    })

describe('Agents CRD generation ownership', () => {
  it('generates every chart CRD from services/agents/api packages', () => {
    const crds = loadCrds()

    expect(crds).toHaveLength(20)
    for (const crd of crds) {
      const packageName = apiPackageFor(crd.group, crd.kind)
      const packageDir = join(apiDir, packageName, 'v1alpha1')
      expect(existsSync(join(packageDir, 'types.go')), `${crd.file} missing ${packageDir}/types.go`).toBe(true)
      expect(existsSync(join(packageDir, 'zz_generated.deepcopy.go')), `${crd.file} missing generated deepcopy`).toBe(
        true,
      )

      const typesSource = readFileSync(join(packageDir, 'types.go'), 'utf8')
      expect(typesSource, `${crd.file} missing Go root type ${crd.kind}`).toContain(`type ${crd.kind} struct`)
      expect(typesSource, `${crd.file} missing Go list type ${crd.kind}List`).toContain(`type ${crd.kind}List struct`)

      const deepcopySource = readFileSync(join(packageDir, 'zz_generated.deepcopy.go'), 'utf8')
      expect(deepcopySource, `${crd.file} not generated by controller-gen`).toContain(
        'Code generated by controller-gen',
      )
      expect(deepcopySource, `${crd.file} missing DeepCopyObject for ${crd.kind}`).toContain(
        `func (in *${crd.kind}) DeepCopyObject() runtime.Object`,
      )
    }
  })

  it('runs controller-gen from the services/agents/api root, not the old Agents-only subpackage', () => {
    const rootGenerate = readFileSync(join(apiDir, 'generate.go'), 'utf8')
    expect(rootGenerate).toContain('paths=./...')
    expect(rootGenerate).toContain('charts/agents/crds')
    expect(rootGenerate).not.toContain('paths=./v1alpha1')
    expect(existsSync(join(apiDir, 'agents/generate.go'))).toBe(false)
  })

  it('keeps CRD filenames aligned with Kubernetes group and plural names', () => {
    for (const crd of loadCrds()) {
      expect(basename(crd.path, '.yaml')).toBe(`${crd.group}_${crd.plural}`)
      expect(crd.name).toBe(`${crd.plural}.${crd.group}`)
      expect(dirname(crd.path)).toBe(crdsDir)
    }
  })
})
